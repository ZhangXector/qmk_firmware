uint16_t get_tapping_term(uint16_t keycode, keyrecord_t *record)
{
	switch (keycode)
	{
		case RCTL_T(KC_QUOT):
			return TAPPING_TERM + 500;
		default:
			return TAPPING_TERM;
	}
}

